    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validateData = $request -> validate([
            'currency_name' => 'required|unique:currencies',
            'currency_abbr' => 'required|unique:currencies',
        ]);

        $merchandisers = new Currency;
        $merchandisers->currency_name = $request->currency_name;
        $merchandisers->currency_abbr = $request->currency_abbr;
        $merchandisers->save();
    }

